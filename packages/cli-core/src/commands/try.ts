import { readConfig } from "../config/io.js";
import { NotInitializedError } from "../errors.js";

// ---------------------------------------------------------------------------
// Types (shared by CLI, MCP, and runner script)
// ---------------------------------------------------------------------------

export interface FieldInfo {
  type: "string" | "number" | "boolean" | "enum" | "object" | "array" | "unknown";
  description?: string;
  required: boolean;
  default?: unknown;
  options?: string[]; // for enum type
}

export interface TryListTool {
  name: string;
  description: string;
  schema: Record<string, FieldInfo>;
}

export interface TryListAgent {
  name: string;
  description: string;
  toolNames: string[];
}

export interface TryListResult {
  tools: TryListTool[];
  agents: TryListAgent[];
}

export interface AgentChatResponse {
  text: string;
  toolCalls: Array<{
    tool: string;
    input: Record<string, unknown>;
    result?: unknown;
  }>;
}

// ---------------------------------------------------------------------------
// Runner command helpers
// ---------------------------------------------------------------------------

export interface TryContext {
  cwd: string;
  runtime: string;
  baseDir: string;
}

/**
 * Read project config and return context needed for `kitn try`.
 */
export async function getTryContext(cwd: string): Promise<TryContext> {
  const config = await readConfig(cwd);
  if (!config) throw new NotInitializedError(cwd);
  return {
    cwd,
    runtime: config.runtime,
    baseDir: config.aliases.base ?? "src/ai",
  };
}

/**
 * Get runtime-specific command to execute the runner script.
 */
export function getRunnerCommand(runtime: string): string[] {
  switch (runtime) {
    case "bun":
      return ["bun"];
    case "deno":
      return ["deno", "run", "--allow-all"];
    case "node":
    default:
      return ["npx", "tsx"];
  }
}

/**
 * Path (relative to project root) where the runner script is written.
 */
